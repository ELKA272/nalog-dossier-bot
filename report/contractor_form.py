def build_form(egrul_data: dict) -> dict:
    form = {}
    for field in FIELDS:
        form[field] = ""
    form["01. Наименование организации"] = egrul_data.get("full_name", "")
    form["02. ИНН"] = egrul_data.get("inn", "")
    form["03. Дата регистрации"] = egrul_data.get("reg_date", "")
    form["04. Вид налогообложения"] = egrul_data.get("tax_regime", "")
    form["05. Юридический адрес"] = egrul_data.get("address", "")
    form["11. Генеральный директор"] = egrul_data.get("director", {}).get("fio", "")
    form["13. Учредители"] = ", ".join(f.get("fio", "") for f in egrul_data.get("founders", []))
    form["15. ОКВЭД"] = egrul_data.get("okved_main", "")
    form["37. ИФНС"] = egrul_data.get("ifns", "")
    return form
